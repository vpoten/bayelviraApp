package elvira.learning.classification;

import elvira.Configuration;
import elvira.database.DataBaseCases;

import java.io.*;
import java.util.Vector;

/*---------------------------------------------------------------*/ 
/**
 * Implementation of classifier interface for a uniform use of the 
 * classification methods.
 * @author J.G. Castellano (fjgc@decsai.ugr.es), Rosa Blanco (rosa@si.ehu.es), Aritz Perez (ccbpemaa@si.ehu.es)
 * @since 23/04/2002
 */

public interface Classifier {
    /** This method is used to build the classifier.
	@param training training set to build the classifier
	@param classnumber number of the variable to classify
    */
    public void learn (DataBaseCases training, int classnumber);

    /** This method is used to classify a instance
	@param instance case to classify
	@param classnumber number of the variable to classify
	@return a Vector with a probability associated to each class
    */
    public Vector<Double> classify (Configuration instance, int classnumber);
    
    public void saveModelToFile(String absolutPath) throws IOException;

 } // End of Classifier interface
